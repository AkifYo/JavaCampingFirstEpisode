package Lesson12_13_Loops;

public class A02_Do_While_Loop {
    public static void main(String[] args) {
        System.out.println();

        //Do-while
        int i=100;
        do {
            System.out.println(i);
            i+=2;
        }while(i<10);
        System.out.println("Dowhile dongusu bitti");
    }
}
